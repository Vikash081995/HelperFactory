//Typeof
//used to get the data type of a variable or value

//String
typeof Date(2011); //string

//Number
typeof 37; //number

//Boolean
typeof true; //boolean

//Object
typeof {}; //object
typeof [1, 2, 3]; //object
typeof null; //object
typeof Error(); //object

//Function
typeof function () {}; //function

//Undefined
typeof undefined; //undefined
